package week1.day1;

public class FindNumber {

	public static void main(String[] args) {
		int number = -10;
		if(number < 0) {
			System.out.println(number + " is negative");
		}else if(number > 0) {
			System.out.println(number + " is positive");
		}else {
			System.out.println(number + " is neutral");
		}
	}

}
